package sidplay.audio.whatssid.fingerprint;

public class Peak {

	private int intFreq;
	private float power;
	private int intTime;

	public int getIntFreq() {
		return intFreq;
	}

	public void setIntFreq(int intFreq) {
		this.intFreq = intFreq;
	}

	public float getPower() {
		return power;
	}

	public void setPower(float power) {
		this.power = power;
	}

	public int getIntTime() {
		return intTime;
	}

	public void setIntTime(int intTime) {
		this.intTime = intTime;
	}

}